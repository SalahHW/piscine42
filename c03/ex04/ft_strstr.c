int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		i++;
	}
	return (i);
}

char	*ft_strstr(char *str, char *to_find)
{
	int	i;
	int	j;

	i = 0;
	if (ft_strlen(to_find) == 0)
	{
		return (str);
	}
	while (str[i])
	{
		j = 0;
		while ((str[i + j] == to_find[j]) && j <= ft_strlen(to_find))
		{
			if (to_find[j + 1] == '\0')
			{
				return (str + i);
			}
			j++;
		}
		i++;
	}
	return (0);
}
